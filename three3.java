public class three3{
	public static void main(String [] args){
		System.out.println("this is another example of while loop");
		int i=0;

		while(i>0){
			System.out.println("this loop has wrong parameters");
			i++;
}
		System.out.println("this loop will not execute because i is not less than 0");
		System.out.println("this is the end of the program");
		
		int j=0;
		while(j<5){
			System.out.println("j = "+ j);
			j++;
			System.out.println("j incremented to " + j);
		}
		System.out.println("while loop ended");
		// This is a simple Java program demonstrating a while loop
}
}